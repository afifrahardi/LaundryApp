class Member {
  final String id;
  final String jenis_kelamin;
  final String nama;
  final String tanggal_lahir;
  final String nomor_telepon;
  final String alamat;
  final String email;

  Member({
    required this.id,
    required this.jenis_kelamin,
    required this.nama,
    required this.tanggal_lahir,
    required this.nomor_telepon,
    required this.alamat,
    required this.email,
  });
}
